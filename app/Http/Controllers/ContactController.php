        Session::flash('full_name', $request->full_name);
        Session::flash('email', $request->email);
        Session::flash('message', $request->message);

        Mail::send('contact.message', $data, function ($message) use ($request){

            $message->from($request->email, $request->full_name);

            $message->to('hhhsoftware2016@gmail.com', 'HHSoftware')->subject('Formulario de Pagina Web');

        });

        return Redirect::back();

    }

